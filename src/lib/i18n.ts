export function t(lang: Lang, key: string): string {
  return UI[lang]?.[key] ?? UI['en'][key] ?? key;
}

export function getTopicLabel(key: string, lang: string): string {
  const spectrum = SPECTRUM[(lang as Lang)] ?? SPECTRUM.en;
  const topic = spectrum.find((s) => s.key === key);
  return topic?.title ?? key;
}

export function getTopicDesc(key: string, lang: string): { low: string; high: string } {
  const spectrum = SPECTRUM[(lang as Lang)] ?? SPECTRUM.en;
  const topic = spectrum.find((s) => s.key === key);
  if (!topic) return { low: '', high: '' };
  return {
    low: topic.left.replace(/\n/g, ' '),
    high: topic.right.replace(/\n/g, ' '),
  };
}
